function quaequad0(mmax::Int, kk::Int)
    # Function to return the requested quadrature rule

    # Check for valid mmax
    if !(1 <= mmax <= 50)
        error("QUAEQUAD0 - Fatal error! Illegal input value of MMAX. 1 <= MMAX <= 50 required.")
    end

    # Get the appropriate rule based on mmax
    rule_function = eval(Symbol("rule", lpad(mmax, 2, '0')))
    xnew, ynew, w = rule_function()

    for i in 1:kk
        # The lower-left 1/6
        iitype = 2
        nbool2 = quaeinside(iitype, xnew[i], ynew[i])

        # The lower 1/3
        iitype = 1
        nbool1 = quaeinside(iitype, xnew[i], ynew[i])

        # The whole triangle
        iitype = 0
        nbool0 = quaeinside(iitype, xnew[i], ynew[i])
        
        if nbool2 == 1
            # Do nothing
        elseif nbool1 == 1
            xnew[i] = -xnew[i]
            # ynew[i] stays the same
        elseif nbool0 == 1
            x0, y0 = xnew[i], ynew[i]
            x1, y1 = quaerotate(x0, y0)
            xnew[i], ynew[i] = x1, y1
        else
            error("QUAEQUAD0 - Fatal error! Point does not lie inside triangle.")
        end
    end

    return xnew, ynew, w
end